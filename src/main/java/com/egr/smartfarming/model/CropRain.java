package com.egr.smartfarming.model;

public class CropRain {


    private double avgRainfall;


    private double minRainfall;

    private double maxRainfall;

    public double getAvgRainfall() {
        return avgRainfall;
    }

    public void setAvgRainfall(double avgRainfall) {
        this.avgRainfall = avgRainfall;
    }

    public double getMinRainfall() {
        return minRainfall;
    }

    public void setMinRainfall(double minRainfall) {
        this.minRainfall = minRainfall;
    }

    public double getMaxRainfall() {
        return maxRainfall;
    }

    public void setMaxRainfall(double maxRainfall) {
        this.maxRainfall = maxRainfall;
    }
}
